    public int uniquePaths(int m, int n, Map<String, Integer> memo) {

        if (m == 1 || n == 1) return 1;
        if (m == 0 || n == 0) return 0;


        String key = m + "," + n;

        if (memo.containsKey(key))
            return memo.get(key);
        else
            memo.put(key, uniquePaths(m, n - 1, memo) + uniquePaths(m - 1, n, memo));

        return memo.get(key);
    }

